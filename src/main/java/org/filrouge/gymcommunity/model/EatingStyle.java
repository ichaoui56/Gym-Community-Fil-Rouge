package org.filrouge.gymcommunity.model;

public enum EatingStyle {
    VEGAN,            // No animal products
    VEGETARIAN,       // No meat, but allows dairy & eggs
    OMNIVORE,         // Eats everything
    KETO,             // Low-carb, high-fat diet
    PALEO,            // Whole foods, no processed food
    MEDITERRANEAN,    // High in healthy fats, fish, and vegetables
    PEDESTRIAN,      // No meat, but eats fish
    CARNIVORE         // All-meat diet, zero carbs
}
